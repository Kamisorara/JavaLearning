package _算法.leetcode.输出整数;

import java.awt.*;

public class Main {
    public static void main(String[] args) {
        for (int i = 0; i < 11; i++) {
            if (i == 10) {
                System.out.print(i);
            } else {
                System.out.print(i + " ");
            }
        }
    }
}
